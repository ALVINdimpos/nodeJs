const mongoose = require('mongoose');

const queriesSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  message: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});

const queriesModel = mongoose.model('Queries', queriesSchema);

module.exports = queriesModel;
